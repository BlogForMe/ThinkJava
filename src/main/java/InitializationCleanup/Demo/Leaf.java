package InitializationCleanup.Demo;

/**
 * Created by Jon on 2016/5/29.
 * 5.4 this 关键字
 */
public class Leaf {
    int i = 0;

    Leaf increment() {
        i++;
        return this;
    }

    void print() {
        System.out.println("i = " + i);
    }

    public static void main(String[] args) {
        Leaf x = new Leaf();
        x.increment().increment().increment().print();
    }
}
